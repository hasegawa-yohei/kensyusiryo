package first;

public class KadaiFor {
	
	static int limit =5;
	public static void main (String[]args) {

	  for(int i =0;i<=9;i++) {
	     System.out.println(i);
	  }
	  
	  for(int i =0;i<limit;i++) {
		  System.out.println(i);
	  }
	  
	  int[]num= {50,20,30,40,10};
	  for(int i =0;i<num.length;i++) {
		     System.out.println(num[i]);
		  }

	}

}
